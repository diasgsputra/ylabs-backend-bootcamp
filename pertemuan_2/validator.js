function cekstring(n){
    if(typeof(n)=="string"){
        return true
    }
    else{
        return false
    }
}

function ceknumber(n){
    if(typeof(n)=="number"){
        return true
    }
    else{
        return false
    }
}

function cekboolean(n){
    if(typeof(n)=="boolean"){
        return true
    }
    else{
        return false
    }
}